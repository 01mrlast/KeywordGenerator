    function last_gethashttag($keyword)
    {
        $keywords = array();
        $data = last_get_data('http://suggestqueries.google.com/complete/search?output=firefox&client=firefox&hl=en-US&q=' . urlencode($keyword));

        if (($data = json_decode($data, true)) !== null) {
            $keywords = $data[1];
        }

        $string = '';
        $i = 1;
        foreach ($keywords as $k) {
            $string .= '#'.url_slug($k) . ', ';
            if ($i++ == 10) break;
        }
        return $string;
    }

